// Biorthogonal and Reverse Biorthogonal wavelet filter coefficients
//
// Biorthogonal wavelets use different filters for decomposition and reconstruction,
// allowing symmetric/linear-phase filters.

use super::WaveletFilters;
use crate::error::{SignalError, SignalResult};

/// Helper to create high-pass filters from low-pass using QMF relationship
fn create_highpass_bior(dec_lo: &[f64], rec_lo: &[f64]) -> (Vec<f64>, Vec<f64>) {
    let mut dec_hi = vec![0.0; rec_lo.len()];
    let mut rec_hi = vec![0.0; dec_lo.len()];

    for i in 0..rec_lo.len() {
        dec_hi[i] = (-1_f64).powi(i as i32) * rec_lo[rec_lo.len() - 1 - i];
    }

    for i in 0..dec_lo.len() {
        rec_hi[i] = (-1_f64).powi(i as i32 + 1) * dec_lo[dec_lo.len() - 1 - i];
    }

    (dec_hi, rec_hi)
}

fn make_bior_result(
    dec_lo: Vec<f64>,
    rec_lo: Vec<f64>,
    nr: usize,
    nd: usize,
) -> SignalResult<WaveletFilters> {
    let (dec_hi, rec_hi) = create_highpass_bior(&dec_lo, &rec_lo);
    Ok(WaveletFilters::new(
        dec_lo,
        dec_hi,
        rec_lo,
        rec_hi,
        &format!("bior{}.{}", nr, nd),
        nr,
    ))
}

