/// Reverse biorthogonal wavelet filters
#[allow(dead_code)]
pub(super) fn rbior_filters(nr: usize, nd: usize) -> SignalResult<WaveletFilters> {
    let valid_combinations = [
        (1, 1),
        (1, 3),
        (1, 5),
        (2, 2),
        (2, 4),
        (2, 6),
        (2, 8),
        (3, 1),
        (3, 3),
        (3, 5),
        (3, 7),
        (3, 9),
        (4, 4),
        (5, 5),
        (6, 8),
    ];

    if !valid_combinations.contains(&(nr, nd)) {
        return Err(SignalError::ValueError(format!(
            "Invalid reverse biorthogonal wavelet specification: rbio{}.{}. Valid combinations are: {:?}",
            nr, nd, valid_combinations
        )));
    }

    // Reverse biorthogonal: get the corresponding bior filters and swap dec/rec
    let bior = bior_filters(nr, nd)?;

    let filters = WaveletFilters::new(
        bior.rec_lo,
        bior.rec_hi,
        bior.dec_lo,
        bior.dec_hi,
        &format!("rbio{}.{}", nr, nd),
        nr,
    );

    Ok(filters)
}
